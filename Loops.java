import java.util.Scanner;

public class Loops {
    public static void main(String[] args) {
        
        //for loop
        for(int k=269; k<=300; k++){
            System.out.println(k);
        }

        //while loop
        int k = 269;
        while(k<=280){
            System.out.println(k);
            k++;
        }

        //do while loop
        int i=10;
        do{
System.out.println(i);
i++;
 
        }while(i<=50);
    
        
    }
    
} 
