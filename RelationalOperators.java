public class RelationalOperators {
    public static void main(String[] args) {
        int x = 5;
         
        //==
        System.out.println(x==5);

        //!=
           System.out.println(x!= 3);
           
           //<=
            System.out.println(x<=5);

            //>=
              System.out.println(x>=5);

              //>
                 System.out.println(x>4);

                 //<
                    System.out.println(x<7);


    }
    
}
